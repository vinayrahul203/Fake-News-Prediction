import pandas as pd

# Load datasets
fake_df = pd.read_csv("Fake.csv", on_bad_lines='skip', engine='python')
true_df = pd.read_csv("True.csv", on_bad_lines='skip', engine='python')

# Add labels: 0 = Fake, 1 = True
fake_df['label'] = 0
true_df['label'] = 1

# Combine and shuffle
data = pd.concat([fake_df, true_df], ignore_index=True).sample(frac=1, random_state=42).reset_index(drop=True)
data = data[['text', 'label']].dropna()

from sklearn.model_selection import train_test_split
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics import accuracy_score

# Models
from sklearn.linear_model import LogisticRegression, PassiveAggressiveClassifier
from sklearn.naive_bayes import MultinomialNB
from sklearn.ensemble import RandomForestClassifier
from sklearn.svm import LinearSVC

# TF-IDF Vectorization
vectorizer = TfidfVectorizer(stop_words='english', max_df=0.7)
X = vectorizer.fit_transform(data['text'])
y = data['label']

# Split
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.25, random_state=42)

# Define models
models = {
    "Logistic Regression": LogisticRegression(max_iter=1000),
    "Naive Bayes": MultinomialNB(),
    "Random Forest": RandomForestClassifier(n_estimators=100),
    "Passive Aggressive": PassiveAggressiveClassifier(max_iter=50),
    "Linear SVC": LinearSVC()
}

# Train and evaluate
results = {}
for name, model in models.items():
    model.fit(X_train, y_train)
    y_pred = model.predict(X_test)
    acc = accuracy_score(y_test, y_pred)
    results[name] = {"model": model, "accuracy": acc}
    print(f"{name} Accuracy: {acc:.4f}")

# Pick best model
best_model_name = max(results, key=lambda k: results[k]['accuracy'])
best_model = results[best_model_name]['model']

print(f"\n✅ Best Model: {best_model_name} ({results[best_model_name]['accuracy']*100:.2f}%)")

# Predict on full data
data['predicted_label'] = best_model.predict(X)
data['predicted_label'] = data['predicted_label'].map({0: 'Fake', 1: 'True'})